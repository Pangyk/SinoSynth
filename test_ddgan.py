def sample_and_test(rank, gpu, args):
    torch.manual_seed(args.seed + rank)
    torch.cuda.manual_seed(args.seed + rank)
    torch.cuda.manual_seed_all(args.seed + rank)
    device = torch.device('cuda:{}'.format(gpu))

    from ma_simul.build_geometry import initialization, imaging_geo_cuda

    n_proj = 256
    param = initialization(n_proj)

    fanbeam = imaging_geo_cuda(param)

    # Head and Neck
    if args.dataset == 'ct':
        real_cbct_dir = '/shenlab/lab_stor/yunkuipa/codes_cvpr/data/h5_cbct/testing_CBCT.h5'
        real_pct_dir = '/shenlab/lab_stor/yunkuipa/codes_cvpr/data/h5_cbct/testing_pCT.h5'
        pid_file = '/shenlab/lab_stor/yunkuipa/codes_cvpr/data/h5_cbct/testing_CBCT_ids.txt'

    # Load data
    cbct_dataset = CBCTDataset(real_cbct_dir, args)
    pct_dataset = CBCTDataset(real_pct_dir, args)

    netG = NCSNpp(args).to(device)
    ckpt = torch.load('./saved_info/dd_gan/{}/{}/netG_{}.pth'.format(args.dataset, args.exp, args.epoch_id),
                      map_location=device)

    # loading weights from ddp in single gpu
    for key in list(ckpt.keys()):
        ckpt[key[7:]] = ckpt.pop(key)
    netG.load_state_dict(ckpt)
    netG.eval()

    netG = nn.parallel.DistributedDataParallel(netG, device_ids=[gpu])

    T = get_time_schedule(args, device)

    pos_coeff = Posterior_Coefficients(args, device)

    save_dir = "./generated_samples/"

    coeff = Diffusion_Coefficients(args, device)

    if not os.path.exists(save_dir):
        os.makedirs(save_dir)

    num_p, num_s = cbct_dataset.get_dim()
    pct_data = pct_dataset.get_cbct_data()
    for pid in range(num_p):
        count, psnr, ssim = 0, 0, 0
        volume = np.zeros([num_s, 256, 256])
        for sid in range(num_s):
            if rank == 0:
                x, tar = cbct_dataset.get_slice(pid, sid)
                if x.min() == x.max():
                    continue
                # print(sid)
                x = x.to(device, non_blocking=True)
                tar = tar.to(device, non_blocking=True)

                t = torch.ones((x.size(0),), dtype=torch.int64, device=device) * args.num_timesteps

                x_t_1 = q_sample_pairs(coeff, x, t)
                res = sample_from_model(pos_coeff, netG, args.num_timesteps, x_t_1, tar, T, args)

                res = torch.clip(res, 0, 1)
                volume[sid] = res[0, 0].detach().cpu().numpy()

        if rank == 0:
            to_nii(volume, pid, pid_file, save_dir)


def init_processes(rank, size, fn, args):
    """ Initialize the distributed environment. """
    os.environ['MASTER_ADDR'] = args.master_address
    os.environ['MASTER_PORT'] = '6020'

    torch.cuda.set_device(args.local_rank)
    gpu = args.local_rank
    dist.init_process_group(backend='nccl', init_method='env://', rank=rank, world_size=size)
    fn(rank, gpu, args)
    dist.barrier()
    cleanup()


def cleanup():
    dist.destroy_process_group()


